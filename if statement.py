# Conditional statements are set of codes which execute a block a code when the condition is true
# 1. if Conditional statement
 # if(condition ): execute something    ---- > syntax

age = int(input("what is your age?"))

if age > 18:
    print("you are eligible to vote")
else :
    print("you are eligible to vote")


# the above is straightforward if statement that execute the  print statement if age is > 18
# else, it does something else




